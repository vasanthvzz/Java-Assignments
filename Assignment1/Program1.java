public class Program1{
    public static void main(String[] args) {

        //Float to integer
        float f = 45.4f;
        int a = (int)f;
        System.out.println(a);
        //Float to double
        double d = (double)f;
        System.out.println(d);
        int i = 120000;
        //int to short
        short j = (short)i;
        System.out.println(j);
        //Double to float
        float f2 = (float)d;
        System.out.println(f2);
    }
}